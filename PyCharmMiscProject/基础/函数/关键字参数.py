# 定义一个函数 person，演示各种参数类型的使用
def person(name, age=30, *args, city, job='程序员', **kwargs):
    """
    参数说明：
    1. name: 位置参数，调用时必须传入。
    2. age=30: 默认参数，如果调用时不传，则默认值为30。
    3. *args: 可变位置参数，接收多个额外的位置参数，形成一个元组。
    4. city: 命名关键字参数，调用时必须以关键字形式传入。
    5. job='程序员': 命名关键字参数，带默认值。
    6. **kwargs: 任意关键字参数，接收多个额外的关键字参数，形成一个字典。
    """

    print(f'姓名: {name}')         # 位置参数
    print(f'年龄: {age}')          # 默认参数
    print(f'其他位置参数 args: {args}')  # 可变位置参数，元组
    print(f'城市: {city}')          # 命名关键字参数，必须传
    print(f'职业: {job}')           # 命名关键字参数，带默认值
    print(f'其他关键字参数 kwargs: {kwargs}')  # 任意关键字参数，字典
    print('------分割线------')

# 调用示例1：只传必需的参数和命名关键字参数
person('小明', city='北京')

# 调用示例2：传入位置参数、默认参数、命名关键字参数和任意关键字参数
person('小红', 25, city='上海', hobby='篮球', married=False)

# 调用示例3：传入可变位置参数和所有其他参数
person('小刚', 40, '喜欢唱歌', '爱好跳舞', city='广州', job='设计师', hobby='摄影')

# 调用示例4：传入所有参数，包含多个可变位置参数和多个任意关键字参数
person('小李', 28, '游泳', '跑步', city='深圳', job='产品经理', hobby='阅读', married=True, children=2)


# 笔记：
# 1. 位置参数必须传，且传参顺序固定。
# 2. 默认参数可以省略，若省略则使用默认值。
# 3. *args 用于接收额外的位置参数，调用时位置参数多余形参时会被收集到 args 元组中。
# 4. 命名关键字参数必须以关键字形式传入，且可以有默认值。
# 5. **kwargs 用于接收额外的关键字参数，调用时未定义的关键字参数会被收集到 kwargs 字典中。
# 6. 组合使用时，参数顺序必须是：位置参数、默认参数、*args、命名关键字参数、**kwargs。
# 7. 调用时可以灵活组合各种参数，满足复杂函数调用需求。
